package com.it.bean;

public class Users {
	private String user_id;//用户编号
	private String roleId = "-1";//角色编号
	private String roleName;//角色名称
	private String user_pwd;//用户密码
	private String user_name;//真实姓名
	private String user_sex;//性别
	private String user_addr;//用户地址
	private String user_phone;//用户电话	
	private String create_time;//创建时间
	private String user_inputer;//备注
	
	
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public Users(String user_pwd, String user_name) {
		super();
		this.user_pwd = user_pwd;
		this.user_name = user_name;
	}
	public String getRoleId() {
		return roleId;
	}
	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getUser_pwd() {
		return user_pwd;
	}
	public void setUser_pwd(String user_pwd) {
		this.user_pwd = user_pwd;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_sex() {
		return user_sex;
	}
	public void setUser_sex(String user_sex) {
		this.user_sex = user_sex;
	}
	public String getUser_addr() {
		return user_addr;
	}
	public void setUser_addr(String user_addr) {
		this.user_addr = user_addr;
	}
	public String getUser_phone() {
		return user_phone;
	}
	public void setUser_phone(String user_phone) {
		this.user_phone = user_phone;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getUser_inputer() {
		return user_inputer;
	}
	public void setUser_inputer(String user_inputer) {
		this.user_inputer = user_inputer;
	}
	public Users(String user_id, String user_pwd, String user_name, String user_sex, String user_addr,
			String user_phone, String create_time, String user_inputer) {
		super();
		this.user_id = user_id;
		this.user_pwd = user_pwd;
		this.user_name = user_name;
		this.user_sex = user_sex;
		this.user_addr = user_addr;
		this.user_phone = user_phone;
		this.create_time = create_time;
		this.user_inputer = user_inputer;
	}
	public Users() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "Users [user_id=" + user_id + ", roleId=" + roleId + ", roleName=" + roleName + ", user_pwd=" + user_pwd
				+ ", user_name=" + user_name + ", user_sex=" + user_sex + ", user_addr=" + user_addr + ", user_phone="
				+ user_phone + ", create_time=" + create_time + ", user_inputer=" + user_inputer + "]";
	}
	
}